# ---------------------------------------------------------------------------
# Time greeting
# ---------------------------------------------------------------------------
def get_time_greeting(hour: int) -> str:
    if 6 <= hour < 12:
        return "สวัสดีตอนเช้า"
    elif 12 <= hour < 14:
        return "สวัสดีตอนเที่ยง"
    elif 14 <= hour < 18:
        return "สวัสดีตอนเย็น"
    else:
        return "สวัสดีตอนค่ำ"


def get_time_emoji(hour: int) -> str:
    if 6 <= hour < 12:
        return "🌅"
    elif 12 <= hour < 14:
        return "☀️"
    elif 14 <= hour < 18:
        return "🌇"
    else:
        return "🌙"


